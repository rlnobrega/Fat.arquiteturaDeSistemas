public class DadosNaoPrimitivos { // Dados não primitivos são objetos
    public static void main(String[] args) { // método principal

    String nome = "Naruto Uzumaki"; // Dados não primitivos são objetos

    String nomeMaiusculo = nome.toUpperCase(); // transforma o texto em maiúsculo

    System.out.println("Este texto está em Maiúsculo " + nomeMaiusculo); // imprime o texto em maiúsculo

    String aldeia = "Aldeia da Folha"; // Dados não primitivos são objetos

    String aldeiaMinusculo = aldeia.toLowerCase(); // transforma o texto em minúsculo

    System.out.println("O nome da Aldeia do " + nome + " está em minúsculo " + aldeiaMinusculo); // imprime o texto em minúsculo

    }
}